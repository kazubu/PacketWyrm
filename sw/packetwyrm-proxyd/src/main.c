static void usage(void) {
    fputs(
        "usage: packetwyrm-proxyd [options]\n"
        "  -c, --config FILE    read options from a config file (key: value;\n"
        "                       keys: listen, socket, tls_cert, tls_key, no_tls,\n"
        "                       insecure_no_auth, allowed_hosts). CLI flags below\n"
        "                       override the file.\n"
        "  --listen ADDR:PORT   bind address (default " PROXYD_DEFAULT_LISTEN ")\n"
        "  --socket PATH        daemon control socket (default "
            PW_IPC_DEFAULT_PATH ")\n"
        "  --tls-cert FILE      TLS certificate (PEM); default self-signed\n"
        "  --tls-key FILE       TLS private key (PEM)\n"
        "  --no-tls             serve plain HTTP (localhost / SSH tunnel only)\n"
        "  --insecure-no-auth   allow non-loopback bind even if the daemon\n"
        "                       has no secret configured\n"
        "  --allowed-host NAME[,NAME...]\n"
        "                       additional Host header values accepted on\n"
        "                       POST /api/rpc (anti DNS-rebinding; localhost,\n"
        "                       127.0.0.1, [::1] and the --listen address are\n"
        "                       always accepted)\n"
        "  -h, --help           this help\n",
        stderr);
}

/* Strict decimal port parse: reject empty, trailing junk (e.g. "8443abc"),
 * overflow, and out-of-range -- atoi() silently accepted all of those. */
static int parse_port_strict(const char *s, int *out) {
    if (!s || !*s) return -1;
    char *e = NULL;
    errno = 0;
    long v = strtol(s, &e, 10);
    if (errno != 0 || *e != '\0' || v <= 0 || v > 65535) return -1;
    *out = (int)v;
    return 0;
}

static int parse_listen(struct proxyd_opts *o, const char *s) {
    /* ADDR:PORT or just PORT. */
    const char *colon = strrchr(s, ':');
    static char abuf[128];
    if (colon) {
        size_t alen = (size_t)(colon - s);
        if (alen >= sizeof(abuf)) return -1;
        memcpy(abuf, s, alen);
        abuf[alen] = '\0';
        o->listen_addr = abuf;
        return parse_port_strict(colon + 1, &o->listen_port);
    }
    return parse_port_strict(s, &o->listen_port);
}

/* Parse a boolean scalar: true/false, yes/no, on/off, 1/0 (case-insensitive).
 * Returns 0 and sets *out on success, -1 on an unrecognized value. */
static int parse_bool(const char *s, bool *out) {
    if (!strcasecmp(s, "true") || !strcasecmp(s, "yes") ||
        !strcasecmp(s, "on")   || !strcmp(s, "1"))  { *out = true;  return 0; }
    if (!strcasecmp(s, "false")|| !strcasecmp(s, "no") ||
        !strcasecmp(s, "off")  || !strcmp(s, "0"))  { *out = false; return 0; }
    return -1;
}

/* Load proxyd options from a small YAML-subset config file: one `key: value`
 * per line, `#` comments and blank lines ignored, optional surrounding quotes
 * on values. Recognized keys mirror the CLI flags:
 *   listen, socket, tls_cert, tls_key, no_tls, insecure_no_auth, allowed_hosts
 * Values seed `o`; explicit CLI flags parsed afterwards override them. String
 * values are strdup'd (config is loaded once at startup). Returns 0 on success,
 * -1 on a read/parse error (with a message on stderr). */
static int load_config_file(struct proxyd_opts *o, const char *path) {
    FILE *f = fopen(path, "r");
    if (!f) { fprintf(stderr, "proxyd: cannot open config %s: %s\n", path, strerror(errno)); return -1; }
    char line[1024];
    int lineno = 0, rc = 0;
    while (fgets(line, sizeof line, f)) {
        lineno++;
        char *p = line;
        while (*p == ' ' || *p == '\t') p++;          /* leading ws */
        if (*p == '#' || *p == '\n' || *p == '\0' || *p == '\r') continue;
        char *colon = strchr(p, ':');
        if (!colon) { fprintf(stderr, "proxyd: %s:%d: expected 'key: value'\n", path, lineno); rc = -1; break; }
        *colon = '\0';
        char *key = p;
        /* trim trailing ws off key */
        char *ke = key + strlen(key);
        while (ke > key && (ke[-1] == ' ' || ke[-1] == '\t')) *--ke = '\0';
        char *val = colon + 1;
        while (*val == ' ' || *val == '\t') val++;    /* leading ws off value */
        /* strip trailing ws / newline */
        char *ve = val + strlen(val);
        while (ve > val && (ve[-1] == '\n' || ve[-1] == '\r' || ve[-1] == ' ' || ve[-1] == '\t')) *--ve = '\0';
        /* strip a single pair of surrounding quotes */
        size_t vl = strlen(val);
        if (vl >= 2 && ((val[0] == '"' && val[vl-1] == '"') || (val[0] == '\'' && val[vl-1] == '\''))) {
            val[vl-1] = '\0'; val++;
        }

        if (!strcmp(key, "listen")) {
            if (parse_listen(o, val) != 0) { fprintf(stderr, "proxyd: %s:%d: bad listen '%s'\n", path, lineno, val); rc = -1; break; }
        } else if (!strcmp(key, "socket")) {
            o->daemon_sock = strdup(val);
        } else if (!strcmp(key, "tls_cert")) {
            o->tls_cert = val[0] ? strdup(val) : NULL;
        } else if (!strcmp(key, "tls_key")) {
            o->tls_key = val[0] ? strdup(val) : NULL;
        } else if (!strcmp(key, "allowed_hosts") || !strcmp(key, "allowed_host")) {
            o->allowed_hosts = strdup(val);
        } else if (!strcmp(key, "no_tls")) {
            if (parse_bool(val, &o->no_tls) != 0) { fprintf(stderr, "proxyd: %s:%d: bad bool '%s'\n", path, lineno, val); rc = -1; break; }
        } else if (!strcmp(key, "insecure_no_auth")) {
            if (parse_bool(val, &o->insecure_no_auth) != 0) { fprintf(stderr, "proxyd: %s:%d: bad bool '%s'\n", path, lineno, val); rc = -1; break; }
        } else {
            fprintf(stderr, "proxyd: %s:%d: unknown key '%s'\n", path, lineno, key); rc = -1; break;
        }
    }
    fclose(f);
    return rc;
}

int main(int argc, char **argv) {
    struct proxyd_opts o = {
        .listen_addr = "0.0.0.0",
        .listen_port = PROXYD_DEFAULT_PORT,
        .daemon_sock = PW_IPC_DEFAULT_PATH,
    };

    /* First pass: if a config file is named, load it so its values seed `o`
     * before the CLI loop below, which then OVERRIDES them (CLI > file). */
    for (int i = 1; i < argc; i++) {
        if ((!strcmp(argv[i], "--config") || !strcmp(argv[i], "-c")) && i + 1 < argc) {
            if (load_config_file(&o, argv[i + 1]) != 0) return 2;
        }
    }

    for (int i = 1; i < argc; i++) {
        const char *a = argv[i];
        if ((!strcmp(a, "--config") || !strcmp(a, "-c")) && i + 1 < argc) {
            i++;   /* consumed in the first pass */
        } else if (!strcmp(a, "--listen") && i + 1 < argc) {
            if (parse_listen(&o, argv[++i]) != 0) {
                fprintf(stderr, "proxyd: bad --listen\n");
                return 2;
            }
        } else if (!strcmp(a, "--socket") && i + 1 < argc) {
            o.daemon_sock = argv[++i];
        } else if (!strcmp(a, "--tls-cert") && i + 1 < argc) {
            o.tls_cert = argv[++i];
        } else if (!strcmp(a, "--tls-key") && i + 1 < argc) {
            o.tls_key = argv[++i];
        } else if (!strcmp(a, "--no-tls")) {
            o.no_tls = true;
        } else if (!strcmp(a, "--insecure-no-auth")) {
            o.insecure_no_auth = true;
        } else if (!strcmp(a, "--allowed-host") && i + 1 < argc) {
            o.allowed_hosts = argv[++i];
        } else if (!strcmp(a, "-h") || !strcmp(a, "--help")) {
            usage();
            return 0;
        } else {
            fprintf(stderr, "proxyd: unknown arg '%s'\n", a);
            usage();
            return 2;
        }
    }
    if ((o.tls_cert && !o.tls_key) || (!o.tls_cert && o.tls_key)) {
        fprintf(stderr, "proxyd: --tls-cert and --tls-key must be paired\n");
        return 2;
    }

    signal(SIGPIPE, SIG_IGN);
    g_daemon_sock = o.daemon_sock;

    /* Split --allowed-host into a NULL-terminated array (read-only once the
     * worker threads start). */
    if (o.allowed_hosts && o.allowed_hosts[0]) {
        char *dup = strdup(o.allowed_hosts);
        if (!dup) { fprintf(stderr, "proxyd: out of memory\n"); return 1; }
        size_t n = 1;
        for (const char *p = dup; *p; p++) n += (*p == ',');
        g_allowed_hosts = calloc(n + 1, sizeof(*g_allowed_hosts));
        if (!g_allowed_hosts) {
            fprintf(stderr, "proxyd: out of memory\n");
            return 1;
        }
        size_t k = 0;
        for (char *tok = strtok(dup, ","); tok; tok = strtok(NULL, ","))
            if (tok[0]) g_allowed_hosts[k++] = tok;
        g_allowed_hosts[k] = NULL;
    }

    /* Resolve the listen address to a numeric IPv4 up front. We only bind
     * AF_INET, so reject anything inet_pton can't parse (e.g. an IPv6 literal
     * or a hostname) rather than silently falling back to 0.0.0.0 -- that
     * fallback would turn a "loopback-looking" --listen into a public bind. */
    struct in_addr listen_ia;
    if (inet_pton(AF_INET, o.listen_addr, &listen_ia) != 1) {
        fprintf(stderr, "proxyd: --listen address must be a numeric IPv4 "
                        "address (got '%s'); IPv6/hostnames are not supported\n",
                o.listen_addr);
        return 2;
    }
    bool loopback = (ntohl(listen_ia.s_addr) >> 24) == 127; /* 127.0.0.0/8 */
    g_listen_addr = o.listen_addr;

    printf("packetwyrm-proxyd starting\n");
    printf("  daemon socket: %s\n", o.daemon_sock);

    /* Auth safeguard: on a non-loopback bind, only proceed if the daemon is
     * KNOWN to require a secret (sec == 1). If it has none (sec == 0) or we
     * can't tell because it's unreachable at startup (sec == -1), fail CLOSED
     * -- otherwise a no-secret daemon coming up later would be exposed. */
    int sec = probe_daemon_secret();
    if (!loopback && !o.insecure_no_auth && sec != 1) {
        fprintf(stderr,
            "proxyd: REFUSING to bind %s:%d -- %s, so a public bind could "
            "expose full control.\n"
            "        Set system.secret in the daemon env config, bind "
            "127.0.0.1, or pass --insecure-no-auth to override.\n",
            o.listen_addr, o.listen_port,
            sec == 0 ? "the daemon has no secret configured"
                     : "the daemon is unreachable so its auth can't be confirmed");
        return 1;
    }
    if (sec == -1)
        fprintf(stderr, "  warning: daemon not reachable at startup "
                        "(will relay once it is up)\n");
    else
        printf("  daemon auth: %s\n", sec == 1 ? "secret required" : "OPEN (no secret)");

    /* Second safeguard: --no-tls on a non-loopback bind sends the access secret
     * (in the JSON body) across the network in PLAINTEXT even when the daemon
     * requires one. --no-tls is meant for localhost / an SSH tunnel; refuse a
     * public plaintext bind unless the operator explicitly overrides. */
    if (!loopback && o.no_tls && !o.insecure_no_auth) {
        fprintf(stderr,
            "proxyd: REFUSING to bind %s:%d with --no-tls -- the access secret "
            "would cross the network in plaintext.\n"
            "        Use TLS (drop --no-tls), bind 127.0.0.1 (localhost / SSH "
            "tunnel), or pass --insecure-no-auth to override.\n",
            o.listen_addr, o.listen_port);
        return 1;
    }

    /* Arm the lazy re-probe (see auth_probe_current). Reaching this point
     * non-loopback without the override means the startup probe passed;
     * seed the freshness window from it. */
    g_probe_enforce = !loopback && !o.insecure_no_auth;
    if (g_probe_enforce && sec == 1) {
        clock_gettime(CLOCK_MONOTONIC, &g_probe_ok_ts);
        g_probe_ok_valid = true;
    }

    /* TLS. */
    if (!o.no_tls) {
        g_ssl_ctx = tls_ctx_new(&o);
        if (!g_ssl_ctx) return 1;
    } else {
        printf("  TLS: DISABLED (--no-tls, plaintext)\n");
    }

    /* Listen socket. On a bind/listen failure, close the socket and free the
     * TLS context before exiting (tidy even though the process is ending). */
    int lfd = socket(AF_INET, SOCK_STREAM | SOCK_CLOEXEC, 0);
    if (lfd < 0) { perror("socket"); if (g_ssl_ctx) SSL_CTX_free(g_ssl_ctx); return 1; }
    int one = 1;
    setsockopt(lfd, SOL_SOCKET, SO_REUSEADDR, &one, sizeof(one));
    struct sockaddr_in sa = { .sin_family = AF_INET,
                              .sin_port = htons((uint16_t)o.listen_port),
                              .sin_addr = listen_ia };
    if (bind(lfd, (struct sockaddr *)&sa, sizeof(sa)) < 0) {
        perror("bind");
        close(lfd); if (g_ssl_ctx) SSL_CTX_free(g_ssl_ctx);
        return 1;
    }
    if (listen(lfd, 16) < 0) {
        perror("listen");
        close(lfd); if (g_ssl_ctx) SSL_CTX_free(g_ssl_ctx);
        return 1;
    }
    printf("  listening on %s://%s:%d/\n", o.no_tls ? "http" : "https",
           o.listen_addr, o.listen_port);
    fflush(stdout);

    for (;;) {
        int cfd = accept(lfd, NULL, NULL);
        if (cfd < 0) {
            if (errno == EINTR) continue;
            perror("accept");
            break;
        }
        if (atomic_load(&g_active_threads) >= PROXYD_MAX_THREADS) {
            struct conn c = { .fd = cfd, .ssl = NULL };
            /* Can't cheaply TLS-negotiate here; just drop over plaintext. */
            if (!g_ssl_ctx) http_error(&c, 503, "too many connections");
            close(cfd);
            continue;
        }
        atomic_fetch_add(&g_active_threads, 1);
        pthread_t th;
        if (pthread_create(&th, NULL, worker, (void *)(intptr_t)cfd) != 0) {
            atomic_fetch_sub(&g_active_threads, 1);
            close(cfd);
            continue;
        }
        pthread_detach(th);
    }

    close(lfd);
    if (g_ssl_ctx) SSL_CTX_free(g_ssl_ctx);
    return 0;
}
